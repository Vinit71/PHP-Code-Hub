<?php
$colors = array("Red", "Green", "Blue", "Yellow"); //indexed array

// Using foreach to iterate over the indexed array
echo "Colors in the array:<br>";
foreach ($colors as $value) {
    echo $value . "<br>";
}

/* foreach loop in PHP simplifies iterating through arrays.

 * It automatically handles indexes. 
  
 * It makes code more readable and concise. */

?>
